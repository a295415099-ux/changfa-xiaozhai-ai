def generate_report(brand, sku, content_title, platform="小红书/抖音", output=None):
    """生成完整的报告模板"""
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M")

    report = TEMPLATE.format(
        brand=brand,
        sku=sku,
        platform=platform,
        content_title=content_title,
        timestamp=timestamp,
    )

    if output:
        with open(output, "w", encoding="utf-8") as f:
            f.write(report)
        print(f"✅ 报告已生成: {output}")
    else:
        print(report)

    return report


def main():
    parser = argparse.ArgumentParser(
        description="AI内容到承接检查表 - 完整报告生成脚本"
    )
    parser.add_argument("--brand", required=True, help="品牌名")
    parser.add_argument("--sku", required=True, help="核心SKU名称")
    parser.add_argument("--content-title", required=True, help="内容标题/ID")
    parser.add_argument("--platform", default="小红书/抖音", help="内容发布平台")
    parser.add_argument("--output", "-o", help="输出文件路径")

    args = parser.parse_args()
    generate_report(
        brand=args.brand,
        sku=args.sku,
        content_title=args.content_title,
        platform=args.platform,
        output=args.output,
    )


if __name__ == "__main__":
    main()
